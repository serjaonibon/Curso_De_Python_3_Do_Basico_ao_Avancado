"""
Repetições
while (enquanto)
Executa uma ação enquanto uma condição for verdadeira
Loop infinito -> Quando um código não tem fim
"""
inicio_do_contador = 0

while inicio_do_contador < 10:
    # inicio_do_contador = inicio_do_contador + 1
    inicio_do_contador += 1 # Incrementa o contador
    print(inicio_do_contador)

print('Acabou')